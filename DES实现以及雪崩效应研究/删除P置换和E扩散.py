def funcf(r,k):
    #删除P置换和E扩散
    rr=E(r)#E扩展改为两端填充0
    listt=[]#存储异或加分组
    for i in range(8):
        listt.append([])
        for j in range(6):
            n=int(rr[6*i+j])^int(k[6*i+j])
            listt[i].append(n)
    #S盒压缩
    rrr=S(listt)
    return rrr         


#DES加密主体部分
def ept(p,k):#一组64bit
    #获取密钥表
    key=key_create(k)
    #初始置换IP
    p=IP(p)
    #左，右
    pl=p[:32]
    pr=p[32:]
    for i in range(6):#只要前六轮
        pl_new=pr
        prk=funcf(pr,key[i])#f函数
        #pl与prk异或（32bit)
        pr_new=''
        for j in range(32):
            n=int(pl[j])^int(prk[j])
            pr_new+=str(n)
        #print('第{}轮密文为{}'.format(i+1,by_16(pl_new+pr_new)))
        pl=pl_new
        pr=pr_new
    print('6轮加密后{}'.format(by_16(pl+pr)))
    #IP逆置换
    cipher=IP_invert(pr+pl)
    return cipher


