def key_create(k):
    #去除校验位+PC-1置换
    kk=PC_1(k)
    c=kk[:28]
    d=kk[28:]
    #16轮
    key=['']*16#密钥表
    for i in range(16):
        c=movel(c,i)
        d=movel(d,i)#循环移位
        key[i]=PC_2(c+d)#PC_2置换
    #print(key)
    return key


